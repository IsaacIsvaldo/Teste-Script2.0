// Função para verificar se um ponto está dentro da zona de Luanda
function verificaPontoNaZona(latitude: number, longitude: number): boolean {
        return luandaZoneCoordinates.some(point => {
        const latDiff = Math.abs(point.latitude - latitude);
        const lonDiff = Math.abs(point.longitude - longitude);
        const tolerance = 0.01;
        return latDiff < tolerance && lonDiff < tolerance;
    });
}

